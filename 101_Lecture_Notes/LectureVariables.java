/* 
    Lecture note example - Variables (String, int, double)
*/

class LectureVariables{
    public static void main(String args[]) {
        String name; 
        name = new String("Jimothy the Seventh");     //Constructing
        
        int num;
        num = 7;
        
        double money;
        money = 100000.99;
        
        System.out.println(name + " lives in a casino");
        System.out.println(name + " plays roulette" + " " + num + " times");
        System.out.println(name + " makes" + " " + money + " " + "dollars!!!");

	}
}